#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	i;

	str = (char *)s;
	i = 0;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
}

int main()
{
	char s[23]= "allo";
	printf("%s\n",s);
	bzero(s,1);
	// allo  \0llo
	printf("%s\n",s + 1);
}